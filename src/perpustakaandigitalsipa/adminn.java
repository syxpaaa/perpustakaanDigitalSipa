    private void muatTabel(){
        String jk="";
        DefaultTableModel model = (DefaultTableModel) tbl.getModel();
        //kosongkan model
        model.getDataVector().removeAllElements();
        //buat query ambil data
        String sql = "select * from admin";
        //buat objek model database
        ModelDataBase md = new ModelDataBase();
        ResultSet rs = md.ambilData(sql);
        try {
            while(rs.next()){
                Vector baris = new Vector();
                baris.add(rs.getInt("id"));
                baris.add(rs.getString("namalengkap"));
                baris.add(rs.getString("password"));
                baris.add(rs.getString("username"));                
                baris.add(rs.getString("jeniskelamin"));
                baris.add(rs.getString("noHp"));
                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(adminn.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        
    }
    
