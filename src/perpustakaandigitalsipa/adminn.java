    private void simMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simMouseClicked
        // TODO add your handling code here:
        String u = user.getText();
        String p = pas.getText();
        String nm = namleng.getText();
        String jk = "";
        if(cwk.isSelected()){
            jk = cwk.getText();
        }else if(cw.isSelected()){
            jk = cw.getText();
        }
       
        String n = hp.getText();
        String sql = "insert into admin(username,password,namalengkap,jeniskelamin,noHp) values('"+u+"','"+p+"','"+nm+"','"+jk+"','"+n+"')";
       
        ModelDataBase kn = new ModelDataBase();
        if(kn.rubahData(sql)){
           status.setText("Data Berhasil Ditambahkan");
           muatTabel();
        }else{
            status.setText("Data Gagal Ditambahkan");
        }
        
        kosong();
    }//GEN-LAST:event_simMouseClicked

    private void ediMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ediMouseClicked
        // TODO add your handling code here:
        int no_baris = tbl.getSelectedRow();
        String kode = tbl.getValueAt(no_baris, 0).toString();
        String u = user.getText();
        String p = pas.getText();
        String nm = namleng.getText();
        String jk = "";
        if(cwk.isSelected()){
            jk = cwk.getText();
        }else if(cw.isSelected()){
            jk = cw.getText();
        }
         String n = hp.getText();
        //buat query update database
        String sql = "update kategoribuku set username='"+u+"', password='"+p+"', namalengkap='"+nm+"', jeniskelamin='"+jk+"', noHp='"+n+"' where id="+kode;
        //periksa update apakah berhasil
         ModelDataBase md = new ModelDataBase();
        if(md.rubahData(sql)){
            status.setText("data berhasil diperbaharui");
            muatTabel();
        }else{
            status.setText("data gagal diperbaharui");
        }
        kosong();
    }//GEN-LAST:event_ediMouseClicked

