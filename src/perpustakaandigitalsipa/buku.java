    private void simMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_simMouseClicked
        // TODO add your handling code here:
        String jd = jud.getText();
        String pn = pen.getText();
        String pr = pener.getText();
        String th = tat.getText();
        String st = stok.getText();
        String kt = (String) kat.getSelectedItem();
        String sql = "insert into buku(judul, penulis, penerbit, tahunterbit, stok, kategori_id) value('"+jd+"','"+pn+"','"+pr+"','"+th+"','"+st+"','"+kt+"')";
        ModelDataBase md = new ModelDataBase();
        if(md.rubahData(sql)){
            status.setText("data berhasil ditambahkan");
            muatTabel();
        }else{
            status.setText("data gagal ditambahkan");
        }
    }//GEN-LAST:event_simMouseClicked

    private void edtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edtMouseClicked
        // TODO add your handling code here:
        int no_baris = tb.getSelectedRow();
        String kode = tb.getValueAt(no_baris, 0).toString();
        String jd = jud.getText();
        String pn = pen.getText();
        String pr = pener.getText();
        String th = tat.getText();
        String st = stok.getText();
        String kt = (String) kat.getSelectedItem();
        //buat query update database
        String sql = "update kategoribuku set judul='"+jd+"',penulis='"+pn+"',penerbit='"+pr+"',tahunterbit='"+th+"',stok='"+st+"',kategori_id='"+kt+"' where id="+kode;
        //periksa update apakah berhasil
         ModelDataBase md = new ModelDataBase();
        if(md.rubahData(sql)){
            status.setText("data berhasil diperbaharui");
            muatTabel();
        }else{
            status.setText("data gagal diperbaharui");
        }
        kosong();
    }//GEN-LAST:event_edtMouseClicked

