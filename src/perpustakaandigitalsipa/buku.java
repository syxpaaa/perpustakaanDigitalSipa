    private  void muatTabel(){
        DefaultTableModel model = (DefaultTableModel) tb.getModel();
        model.getDataVector().removeAllElements();
        String sql = "select*from buku";
        
        ModelDataBase md = new ModelDataBase();
        ResultSet rs = md.ambilData(sql);
        
        try {
            while(rs.next()){
                Vector baris = new Vector();
                baris.add(rs.getInt("id"));
                baris.add(rs.getString("judul"));
                baris.add(rs.getString("penulis"));
                baris.add(rs.getString("penerbit"));
                baris.add(rs.getString("tahunterbit"));
                baris.add(rs.getString("stok"));
                baris.add(rs.getString("kategori_id"));
                model.addRow(baris);
            }
        } catch (Exception e) {
            Logger.getLogger(buku.class.getName()).log(Level.SEVERE, null, e);
        }
        
    }
    
     public void  muatkategori(){
        ModelDataBase model = new ModelDataBase();
        String sql = "select * from kategoribuku";
        ResultSet rs = model.ambilData(sql);
        try {
            while (rs.next()) {                
                kat.addItem(String.valueOf(rs.getInt("id")));
            }
        } catch (SQLException e) {
            Logger.getLogger(buku.class.getName()).log(Level.SEVERE,null,e);
        }
    }
    
